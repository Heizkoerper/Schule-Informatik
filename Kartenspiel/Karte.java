public class Karte {

    private String farbe;
    private String bild;

    public Karte(String farbe, String bild) {

        this.farbe = farbe;
        this.bild = bild;
        
    }

    public String getFarbe() {
        return farbe;
    }

    public String getBild() {
        return bild;
    }
}
